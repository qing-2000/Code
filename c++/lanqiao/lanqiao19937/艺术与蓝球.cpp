#include <stdio.h>
#include <stdlib.h>

// 定义每个月的天数，arr[0]和arr[13]未使用，arr[2]初始为28（平年2月）
int arr[14] = { 0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31, 0 };

// 定义数字0~9的权值：0→13，1→1，2→2，...，9→2
int ass[10] = { 13, 1, 2, 3, 5, 4, 4, 2, 2, 2 };

int main(int argc, char* argv[]) {
    int cnt = 0; // 统计满足条件的天数
    int c;       // 临时变量，用于分解数字

    // 遍历年份：2000~2024
    for (int n = 2000; n <= 2024; n++) {
        c = n;
        int sum = 0; // 计算年份的权值和

        // 分解年份的每一位数字，累加其权值
        while (c > 0) {
            sum = sum + ass[c % 10]; // 取最后一位数字，查表得到权值
            c /= 10;                // 去掉最后一位
        }

        // 判断闰年，更新2月天数
        if ((n % 4 == 0 && n % 100 != 0) || n % 400 == 0) {
            arr[2] = 29; // 闰年2月29天
        }
        else {
            arr[2] = 28; // 平年2月28天
        }

        // 遍历月份：1~12
        for (int i = 1; i <= 12; i++) {
            int sun = sum; // 年份权值 + 月份权值

            // 计算月份的权值（补前导0，如1月→01）
            sun = sun + ass[i % 10]; // 月份的个位
            sun = sun + ass[i / 10];  // 月份的十位（若月份<10，则十位为0，权值13）

            // 遍历当前月的每一天
            for (int j = 1; j <= arr[i]; j++) {
                int sub = sun; // 年+月权值 + 日权值

                // 计算日的权值（补前导0，如1日→01）
                sub = sub + ass[j % 10]; // 日的个位
                sub = sub + ass[j / 10]; // 日的十位

                // 终止条件：2024年4月13日输出结果并退出
                if (n == 2024 && i == 4 && j == 13) {
                    printf("%d", cnt);
                    return 0;
                }

                // 检查总权值是否超过50
                if (sub > 50) {
                    cnt++; // 满足条件，计数+1
                }
            }
        }
    }
    return 0;
}